package com.rom.works.operators;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 9;
        int b = 3;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("================================+ Adds values on either side of the operator.");
        System.out.println("a+b\t\t=" + (a + b));
        System.out.println("================================- Subtracts right-hand operand from left-hand operand.");
        System.out.println("a-b\t\t=" + (a - b));
        System.out.println("================================* Multiplies values on either side of the operator.");
        System.out.println("a*b\t\t=" + (a + b));
        System.out.println("================================/ Divides left-hand operand by right-hand operand.");
        System.out.println("a/b\t\t=" + (a + b));
        System.out.println("================================% Divides left-hand operand by right-hand operand and returns remainder.");
        System.out.println("a%b\t\t=" + (a + b));
        System.out.println("================================++ Increases the value of operand by 1.");
        System.out.println("++a\t\t=" + (++a));
        System.out.println("a++\t\t=" + (a++));
        System.out.println("================================-- Decreases the value of operand by 1.");
        System.out.println("--a\t\t=" + (--a));
        System.out.println("a--\t\t=" + (a--));
    }
}
